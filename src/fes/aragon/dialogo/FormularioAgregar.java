    public void llenarDatosModificar(){
        int fila = this.tabla.getjTable1().getSelectedRow();
        DatosGenerales tmp = ListaManga.getListaManga().getElementos().get(fila);
        
        this.txtNombre.setText(tmp.getNombre());
        this.txtGenero.setText(tmp.getGenero());
        this.txtAutor.setText(tmp.getAutor());
        this.txtAnio.setText(String.valueOf(tmp.getAnio()));
        this.txtNumTomos.setText(String.valueOf(tmp.getNumTomos()));
        this.txtNumCap.setText(String.valueOf(tmp.getNumCapitulos()));
        this.txtRank.setText(String.valueOf(tmp.getOpiniones().getRanking()));
        this.txtCal.setText(String.valueOf(tmp.getOpiniones().getCalificacion()));
        this.txtADescripcion.setText(tmp.getOpiniones().getDescripcion());
    }

