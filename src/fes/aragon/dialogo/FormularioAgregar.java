    private void crearObjeto() {
        DatosGenerales datos = new DatosGenerales();
        datos.setNombre(this.txtNombre.getText());
        datos.setGenero(this.txtGenero.getText());
        datos.setAutor(this.txtAutor.getText());
        datos.setAnio(Integer.parseInt(this.txtAnio.getText()));
        datos.setNumTomos(Integer.parseInt(this.txtNumTomos.getText()));
        datos.setNumCapitulos(Integer.parseInt(this.txtNumCap.getText()));
        datos.getOpiniones().setRanking(Integer.parseInt(this.txtRank.getText()));
        datos.getOpiniones().setCalificacion(Float.parseFloat(this.txtCal.getText()));
        datos.getOpiniones().setDescripcion(this.txtADescripcion.getText());
        if (archivo != null) {
            Toolkit kit = Toolkit.getDefaultToolkit();
            Image img = kit.getImage(archivo.getPath());
            ImageIcon icono = new ImageIcon(img);
            datos.setImagen(icono);
        }
        if(tabla == null){
            ListaManga.getListaManga().getElementos().add(datos);
        } else {
            int fila = this.tabla.getjTable1().getSelectedRow();
            if(archivo == null){
                ImageIcon img = ListaManga.getListaManga().getElementos().get(fila).getImagen();
                datos.setImagen(img);
            }
            ListaManga.getListaManga().getElementos().set(fila, datos);
            ((TablaModelo)tabla.getjTable1().getModel()).fireTableDataChanged();
            tabla.getjTable1().changeSelection(fila, 0, false, false);
            tabla.getjTabbedPane1().setEnabledAt(1, false);
            tabla.getjTabbedPane1().setSelectedIndex(0);
        }
    }

